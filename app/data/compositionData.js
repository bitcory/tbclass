// ── 헬퍼 함수 ──

export function getTechniquesByCategory(categoryId) {
  return compositionTechniques.filter(t => t.category === categoryId);
}

export function getAllTechniques() {
  return compositionTechniques;
}

export function getTechniqueById(id) {
  return compositionTechniques.find(t => t.id === id) || null;
}

export function getCategoryById(id) {
  return compositionCategories.find(c => c.id === id) || null;
}
